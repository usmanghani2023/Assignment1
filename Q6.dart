void main() {
  var alphabet = 'u';

  if (alphabet == 'a' ||
      alphabet == 'e' ||
      alphabet == 'i' ||
      alphabet == 'o' ||
      alphabet == 'u') {
    print('The alphabet $alphabet is a vowel');
  } else {
    // The alphabet is a consonant
    print('The alphabet $alphabet is a consonant');
  }
}
